//Basic Arithmetic operators
let x = 60
let y = 20
console.log("Subtraction of x from y = ", x - y);
console.log("Product of x from y = ", x * y);
console.log("Division of x from y = ", x / y);
console.log("Modulus of x from y = ", x % y);//get the remainder

//Increment Operators pre and post
console.log("Value of x = ", x);
console.log("Printing out x++ = ", x++);

console.log("Value of x = ", x);
console.log("Printing out ++x = ", ++x);

//Decrement operators pre and post
console.log("Value of x = ", x);
console.log("Printing out x-- = ", x--);

console.log("Value of x = ", x);
console.log("Printing out --x = ", --x);

//Increment/Decrement and assign
y = x;
console.log("Value of x is assigned to y = ", y);
x += 1;
console.log("Value of x after x += ", x);
y -= 1;
console.log("Value of y after y -= 1", y);

